/**
 * Fun√ß√£o tradutora principal.
 * @param key A chave da string a ser traduzida.
 * @param vars Um objeto com vari√°veis para substituir na string (ex: {nome: 'Mundo'}).
 * @returns A string traduzida. Se n√£o encontrar, tenta o portugu√™s como fallback e, por √∫ltimo, retorna a pr√≥pria chave.
 */
export function t(key: string, vars?: Record<string, string | number>): string {
    let text = translations[language]?.[key] || translations['pt-br']?.[key] || key;

    if (vars) {
        for (const varKey in vars) {
            const regex = new RegExp(`{${varKey}}`, 'g');
            text = text.replace(regex, String(vars[varKey]));
        }
    }
    return text;
}